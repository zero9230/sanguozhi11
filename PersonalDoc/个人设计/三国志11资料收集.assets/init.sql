CREATE TABLE person (
    id varchar(255) COMMENT '编号',
    name varchar(255) COMMENT '姓名',
    character varchar(255) COMMENT '字',
    power varchar(255) COMMENT '势力',
    legion varchar(255) COMMENT '军团',
    belong varchar(255) COMMENT '所属',
    location varchar(255) COMMENT '所在',
    identity varchar(255) COMMENT '身份',
    loyalty varchar(255) COMMENT '忠诚',
    merit varchar(255) COMMENT '功绩',
    command varchar(255) COMMENT '统率',
    FORCE varchar(255) COMMENT '武力',
    intelligence varchar(255) COMMENT '智力',
    politics varchar(255) COMMENT '政治',
    charm varchar(255) COMMENT '魅力',
    five_circles varchar(255) COMMENT '五围合',
    spearman varchar(255) COMMENT '枪兵',
    halberd varchar(255) COMMENT '戟兵',
    crossbowmen varchar(255) COMMENT '弩兵',
    cavalry varchar(255) COMMENT '骑兵',
    weapon varchar(255) COMMENT '兵器',
    navy varchar(255) COMMENT '水军',
    strong_id varchar(255) COMMENT '强势ID',
    icon varchar(255) COMMENT '头像',
    gender varchar(255) COMMENT '性别',
    phase varchar(255) COMMENT '相性',
    stunt varchar(255) COMMENT '特技',
    stunt_combo varchar(255) COMMENT '特技组合',
    stunt_description varchar(255) COMMENT '特技说明',
    power_level_1 varchar(255) COMMENT '功阶1',
    power_level_2 varchar(255) COMMENT '功阶2',
    power_level_3 varchar(255) COMMENT '功阶3',
    power_level_4 varchar(255) COMMENT '功阶4',
    power_level_5 varchar(255) COMMENT '功阶5',
    power_level_6 varchar(255) COMMENT '功阶6',
    debut_year varchar(255) COMMENT '登场年',
    birth_year varchar(255) COMMENT '出生年',
    death_year varchar(255) COMMENT '死亡年'
);